const NINJA2_SPRITE_ROOT = 'Skills/Ninja2/';

function ninja2Recipe({
  id,
  skillId,
  name,
  role,
  tier,
  sprite,
  timeline,
  durationMs,
  loop = false,
  palette,
  emitters,
  graphics,
  notes,
}) {
  return {
    schemaVersion: 1,
    id: `ninja2_${id}`,
    name,
    durationMs,
    loop,
    palette,
    sampleGroup: 'ninja2_skill_fx',
    sourceSkill: {
      gameId: 'ninja2',
      skillId,
      role,
      tier,
      status: 'draft',
      sprite: `${NINJA2_SPRITE_ROOT}${sprite}`,
      timeline,
    },
    emitters,
    graphics,
    review: {
      status: 'draft',
      notes: `NINJA2 DRAFT: ${notes}`,
    },
  };
}

function burst(id, anchor, texture, quantity, tint, overrides = {}) {
  return {
    id,
    mode: 'burst',
    anchor,
    texture,
    blend: 'ADD',
    quantity,
    lifespan: { min: 180, max: 520 },
    speed: { min: 110, max: 260 },
    angle: { min: 0, max: 360 },
    scale: { start: 0.36, end: 0.03 },
    alpha: { start: 0.95, end: 0 },
    tint,
    gravity: { x: 0, y: 80 },
    ...overrides,
  };
}

function flow(id, anchor, texture, quantity, tint, overrides = {}) {
  return {
    id,
    mode: 'flow',
    anchor,
    texture,
    blend: 'ADD',
    quantity,
    frequency: 48,
    lifespan: { min: 360, max: 860 },
    speed: { min: 50, max: 150 },
    angle: { min: 0, max: 360 },
    scale: { start: 0.32, end: 0.04 },
    alpha: { start: 0.72, end: 0 },
    tint,
    gravity: { x: 0, y: -30 },
    durationMs: 760,
    ...overrides,
  };
}

function graphic(id, type, anchor, color, overrides = {}) {
  return {
    id,
    type,
    anchor,
    color,
    alpha: { start: 0.82, end: 0 },
    radius: { start: 18, end: 88 },
    stroke: 5,
    durationMs: 360,
    ...overrides,
  };
}

