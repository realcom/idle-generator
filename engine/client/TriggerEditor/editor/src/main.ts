ipcMain.on('importWorkspacePatchResources', (event) => {
  // read workspace file
  const wsFile = path.join(workspaceDirectory, 'workspace.triggerws');
  let wsData = null;
  try {
    wsData = fs.readFileSync(wsFile, 'utf-8');
  } catch (e) {
    wsData = '{"activeTrigger": 0}';
  }
  let ws: any;
  try {
    ws = JSON.parse(wsData);
  } catch (e) {
    console.error(e);
    ws = {activeTrigger: 0};
  }
  ws.workspaces = [];
  ws.triggers = [];

  const files = fs.readdirSync(workspaceDirectory);

  const resultTriggers: any[] = [];
  const resultWorkspaces: any[] = [];

  for (const file of files) {
    if (!file.endsWith('.ws')) continue;
    console.log(file);
    const triggerFile = path.join(workspaceDirectory, `${file}`);
    const triggerData = fs.readFileSync(triggerFile, 'utf-8');
    try {
      const triggerJson = JSON.parse(triggerData);
      if ('metadata' in triggerJson) {
        resultTriggers.push(triggerJson.metadata);
      } else {
        const metadata = {
          'name': file.split('.')[0],
          'period': '0',
          'triggerType': '0',
        }
        resultTriggers.push(metadata);
        triggerJson.metadata = metadata;
      }
      resultWorkspaces.push(triggerJson);
    } catch (e) {
      console.error('Failed loading trigger file: ', triggerFile);
      console.error(e);
    }
  }

  resultTriggers.sort((a: any, b: any) => resultTriggers[a.name] - resultTriggers[b.name]);
  resultWorkspaces.sort((a: any, b: any) => resultWorkspaces[a.metadata.name] - resultWorkspaces[b.metadata.name]);

  ws.triggers = resultTriggers;
  ws.workspaces = resultWorkspaces;
  event.returnValue = JSON.stringify(ws);
});


ipcMain.on('loadSettings', async (event, data) => {
  const windowBounds = await settings.get('windowBounds') as any as WindowBounds || defaultWindowBounds;
  event.returnValue = {
    'leftPanelWidth': windowBounds.leftPanelWidth,
  };
});

ipcMain.handle('saveSettings', async (event, data) => {
  const windowBounds = await settings.get('windowBounds') as any as WindowBounds || defaultWindowBounds;
  windowBounds.leftPanelWidth = data.leftPanelWidth;
  try {
    await settings.set('windowBounds', windowBounds as any);
  } catch (e) {
    console.error(e);
  }
});

ipcMain.handle('watchWorkspace', async (event, callback) => {
  watchWorkspace(workspaceFile);
});

ipcMain.handle('unwatchWorkspace', async (event, callback) => {
  unwatchWorkspace(workspaceFile);
});

ipcMain.on('save-file', (event, filePath, content) => {
  fs.writeFile(filePath, content, 'utf-8', (err) => {
    if (err) {
      event.sender.send('save-file-response', {status: 'error', message: err.message});
    } else {
      event.sender.send('save-file-response', {status: 'success', filePath: filePath});
    }
  });
});

ipcMain.handle('show-save-dialog', async (event, filter) => {
  const result = await dialog.showSaveDialog(mainWindow, {
    filters: [
      filter
    ]
  });

  if (!result.canceled && result.filePath) {
    return result.filePath;
  } else {
    return null;
  }
});

ipcMain.handle('show-open-dialog', async (event, filter) => {
  const result = await dialog.showOpenDialog(mainWindow, {
    filters: [
      filter
    ]
  });

  if (!result.canceled && result.filePaths) {
    const content = fs.readFileSync(result.filePaths[0], 'utf-8');
    return {filePath: result.filePaths[0], content: content};
  } else {
    return {filePath: null, content: null};
  }
});