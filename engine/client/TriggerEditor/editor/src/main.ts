interface WindowBounds {
  width: number;
  height: number;
  leftPanelWidth: number;
}

const defaultWindowBounds: WindowBounds = {width: 1200, height: 1000, leftPanelWidth: 900};

// This allows TypeScript to pick up the magic constants that's auto-generated by Forge's Webpack
// plugin that tells the Electron app where to look for the Webpack-bundled app code (depending on
// whether you're running in development or production).
declare const MAIN_WINDOW_WEBPACK_ENTRY: string;
declare const MAIN_WINDOW_PRELOAD_WEBPACK_ENTRY: string;

// Handle creating/removing shortcuts on Windows when installing/uninstalling.
if (require('electron-squirrel-startup')) {
  app.quit();
}

export let mainWindow: BrowserWindow;

const isMac = process.platform === 'darwin';

const workspaceFile = '../data/workspace.triggerws';
const workspaceDirectory = '../data';

const killAPI = 'http://localhost:8090/kill';
const heartbeatAPI = 'http://localhost:8090/heartbeat';
let heartbeatInterval: any;

let workspaceDataLast = '';

const createWindow = async () => {
  const windowBounds: WindowBounds = (await settings.get('windowBounds') as any as WindowBounds) || defaultWindowBounds;

  // Create the browser window.
  mainWindow = new BrowserWindow({
    width: windowBounds.width,
    height: windowBounds.height,
    minWidth: 1100,
    minHeight: 600,
    webPreferences: {
      preload: MAIN_WINDOW_PRELOAD_WEBPACK_ENTRY,
    },
    backgroundColor: '#222',
    title: 'Trigger Editor',
  });

  // and load the index.html of the app.
  mainWindow.loadURL(MAIN_WINDOW_WEBPACK_ENTRY).then(async () => {
    const systemPython = getSystemPython();

    if (systemPython == null) {
      return;
    }

    await installPythonDependencies(systemPython);
    await createCompilerProc(systemPython);
    //reloadWorkspace(workspaceFile);
    watchWorkspace(workspaceFile);
  });

  mainWindow.on('resize', async () => {
    const bounds = mainWindow.getBounds();
    const windowBounds = await settings.get('windowBounds') as any as WindowBounds || defaultWindowBounds;
    windowBounds.width = bounds.width;
    windowBounds.height = bounds.height;
    try {
      settings.set('windowBounds', windowBounds as any);
    } catch (e) {
      console.error(e);
    }
  });

  mainWindow.on('close', () => {
    unwatchWorkspace(workspaceFile);
    mainWindow.webContents.send('exportWorkspacePatchResources');
  });

  // unregister shortcuts on window blur
  mainWindow.on('blur', () => {
    unwatchWorkspace(workspaceFile);
    //mainWindow.webContents.send('exportWorkspacePatchResources');
    watchWorkspace(workspaceFile);
    globalShortcut.unregisterAll();
  });

  // register shortcuts on window focus
  mainWindow.on('focus', () => {
    globalShortcut.register('CommandOrControl+S', () => {
      mainWindow.webContents.send('saveAll');
    });
    globalShortcut.register('CommandOrControl+Z', () => {
      mainWindow.webContents.send('undo');
    });
    globalShortcut.register('CommandOrControl+Shift+Z', () => {
      mainWindow.webContents.send('redo');
    });
  });

  const menu = Menu.buildFromTemplate(template as any);
  Menu.setApplicationMenu(menu);

};

let compilerProc: ChildProcessWithoutNullStreams = null;

function reloadWorkspace(workspacePath: string) {
  try {
    const wsData = fs.readFileSync(workspacePath, 'utf-8');
    if (wsData === workspaceDataLast) return false;
    workspaceDataLast = wsData;
    if (wsData !== '')
      mainWindow.webContents.send('loadWorkspace', wsData);
    return true;
  } catch (e) {
    mainWindow.webContents.send('alert', editorStrings.failedToLoadWorkspace);
    return false;
  }
}

function watchWorkspace(workspacePath: string) {
  return;
  console.log('watch')
  fs.watchFile(workspacePath, (eventType, filename) => {
    if (filename) {
      if (reloadWorkspace(workspacePath)) mainWindow.webContents.send('alert', editorStrings.reloadedWorkspace);
    }
  });
}

function unwatchWorkspace(workspacePath: string) {
  return;
  fs.unwatchFile(workspacePath);
}

function getSystemPython() {
  let systemPython = 'python3';
  try {
    execSync('python3 --version', {stdio: 'ignore'});
  } catch (e) {
    try {
      execSync('python --version', {stdio: 'ignore'});
      systemPython = 'python';
    } catch (e) {
      mainWindow.webContents.send('alert', editorStrings.pythonNotFound);
      systemPython = null;
    }
  }
  return systemPython;
}

function getSystemPythonVersion() {
  const systemPython = getSystemPython();
  if (systemPython == null) return null;
  const version = execSync(`${systemPython} --version`).toString().trim().split(' ')[1].split('.').slice(0, 2).join('.');
  return version;
}

async function installPythonDependencies(systemPython: string) {
  exec(`${systemPython} -c "import flask"`, (error, stdout, stderr) => {
    if (error) {
      if (stderr.includes(`ModuleNotFoundError`)) {
        const result = dialog.showMessageBoxSync(mainWindow, {
          type: 'info',
          title: editorStrings.moduleNotFoundTitle,
          message: editorStrings.moduleNotFoundMessage,
          buttons: [editorStrings.yes, editorStrings.no]
        });
        if (result === 0) {
          exec(`${systemPython} -m pip install flask --break-system-packages --user`, (error, stdout, stderr) => {
            if (error) {
              // retry: for python <3.10
              exec(`${systemPython} -m pip install flask --user`, (error, stdout, stderr) => {
                if (error) {
                  mainWindow.webContents.send('alert', editorStrings.installFailed);
                  console.log(error);
                } else {
                  mainWindow.webContents.send('alert', editorStrings.moduleInstalled);
                  app.exit();
                }
              });
            } else {
              mainWindow.webContents.send('alert', editorStrings.moduleInstalled);
              app.exit();
            }
          });
        }
      }
    }
  });
}

async function createCompilerProc(systemPython: string) {
  let script = ''
  let cnt = 0;
  while (!fs.existsSync(path.join(script, process.resourcesPath, 'compiler', 'app.py')) && cnt < 20) {
    script = path.join('..', script);
    cnt++;
  }
  script = path.join(script, process.resourcesPath, 'compiler', 'app.py');

  console.log('Launching compiler process');

  if (!fs.existsSync(script)) {
    script = '';
    cnt = 0;
    while (!fs.existsSync(path.join(script, 'compiler', 'app.py')) && cnt < 20) {
      script = path.join('..', script);
      cnt++;
    }
    script = path.join(script, 'compiler', 'app.py');
    if (!fs.existsSync(script) && !isMac) {
      mainWindow.webContents.send('alert', editorStrings.failedToFindCompilerScript);
      return;
    }
  }

  exitCompilerProc();

  compilerProc = spawn(`${systemPython}`, [script]);
  console.log(script);
  if (compilerProc != null) {
    if (heartbeatInterval != null) clearInterval(heartbeatInterval);
    heartbeatInterval = setInterval(() => {
      fetch(heartbeatAPI).catch((e) => {
        console.error(e);
      });
    }, 1000);
    console.log('compiler process started');
    console.log('pid: ', compilerProc.pid);
    compilerProc.on('close', (code) => {
      //mainWindow.webContents.send('alert', editorStrings.compilerProcessExited);
    });
    compilerProc.stderr.on('data', (data) => {
      console.error(data.toString().trim());
    });
    compilerProc.stdout.on('data', (data) => {
      console.log(data.toString().trim());
    });
  } else {
    // send ipc event
    mainWindow.webContents.send('alert', editorStrings.failedToStartCompilerProcess);
  }

  setInterval(() => {
    if (compilerProc != null && compilerProc.exitCode != null) {
      console.log('Compiler process is unexpectedly terminated');
      exitCompilerProc();
      createCompilerProc(systemPython);
    }
  }, 3000);
}

const exitCompilerProc = () => {
  if (compilerProc == null) return;
  console.log('killing compiler process via api');
  fetch(killAPI, {method: 'POST'}).catch((e) => {
    console.error(e);
    try {
      console.log('killing compiler process via pid');
      console.log('pid: ', compilerProc.pid);
      kill(compilerProc.pid, 'SIGINT', (err) => {
        console.log(err);
      });
      compilerProc.kill('SIGINT');
    } catch (e) { /* empty */ }
  });
  compilerProc = null;
}

function prepareExit() {
  exitCompilerProc();
  globalShortcut.unregisterAll();
}

// This method will be called when Electron has finished
// initialization and is ready to create browser windows.
// Some APIs can only be used after this event occurs.
app.on('ready', createWindow);
app.on('will-quit', prepareExit);

// Quit when all windows are closed, except on macOS. There, it's common
// for applications and their menu bar to stay active until the user quits
// explicitly with Cmd + Q.
app.on('window-all-closed', () => {
  app.quit();
});

app.on('activate', () => {
  // On OS X it's common to re-create a window in the app when the
  // dock icon is clicked and there are no other windows open.
  if (BrowserWindow.getAllWindows().length === 0) {
    createWindow();
  }
});

