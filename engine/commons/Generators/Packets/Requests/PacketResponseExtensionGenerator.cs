using System;
using System.Collections.Generic;
using System.IO;
using System.Text;

internal class PacketResponseExtensionGenerator
{
    private static void Main(string[] args)
    {
        Console.WriteLine("Generating packet response extension");
        var workingDirectory = Directory.GetCurrentDirectory().Split("Generators")[0] + @"Packets\Requests\";
        var protoPath = $@"{workingDirectory}\Requests.proto";
        Console.WriteLine("proto path: " + protoPath);

        if (!File.Exists(protoPath))
        {
            Console.WriteLine("😒 Proto file not found");
            return;
        }

        var messageNames = new List<string>();
        using (var reader = new StreamReader(protoPath))
        {
            while (reader.ReadLine() is { } line)
            {
                if (line.Contains(".Response"))
                {
                    var responseMessageName = line.Substring(0, line.IndexOf(".Response", StringComparison.Ordinal)).Trim();
                    messageNames.Add(responseMessageName);
                }
            }
        }

        var sb = new StringBuilder();
        sb.AppendLine("// <auto-generated>");
        sb.AppendLine("//  This code was generated by a tool.");
        sb.AppendLine("//");
        sb.AppendLine("//  Changes to this file may cause incorrect behavior and will be lost if");
        sb.AppendLine("//  the code is regenerated.");
        sb.AppendLine("// </auto-generated>");
        sb.AppendLine("namespace Commons.Packets.Requests");
        sb.AppendLine("{");
        foreach (var messageName in messageNames)
        {
            sb.AppendLine($"   public partial class {messageName} {{ public static partial class Types {{ public partial class Response : IPacketResponse {{ }} }} }}");
        }
        sb.AppendLine("}");


        var outputFilePath = $@"{workingDirectory}\Requests.Response.generated.cs";
        
        if (Path.GetDirectoryName(outputFilePath) is { } directoryName)
        {
            Directory.CreateDirectory(directoryName);
            File.WriteAllText(outputFilePath, sb.ToString());
            
            Console.WriteLine($"👍 Successfully generated packet response extension. generate to {outputFilePath}");
        }
        else
        {
            Console.WriteLine($"😒 Failed to generate packet response extension. generate to {outputFilePath}");
        }
        
    }
}